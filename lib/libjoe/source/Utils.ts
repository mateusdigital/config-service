// -----------------------------------------------------------------------------
class Utils
{
  // ---------------------------------------------------------------------------
  static IsNumeric(str: any)
  {
    return !isNaN(str) && !isNaN(parseFloat(str));
  }

  // ---------------------------------------------------------------------------
  static IsInteger(str: any)
  {
    if(!Utils.IsNumeric(str)) {
      return false;
    }

    return parseFloat(str) == parseInt(str);
  }
}

// -----------------------------------------------------------------------------
export default Utils;